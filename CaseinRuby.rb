# Code for case in ruby

print "Enter your day"
day=gets.chomp.to_i
case day
when "Monday"
	puts "The day is Tuesday"

when "Tuesday"
	puts "The day is Monday"

when "Wed"
	puts "The day is Monday"
	
when "Friday"
	puts "The day is Monday"

when "Saturday"
	puts "The day is Monday"

else
	puts "The day is another day"


end